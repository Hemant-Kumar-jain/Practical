package Control_Statement;

public class NewPattern {
	public static void main(String[] args) {
	for(int i=5;i>=1;i--)
	{
		if(i%2==1)
		{
			for(int j=5;j>=i;j--) {
				System.out.print("* ");
		}
		System.out.println();
	}}
	
	for(int i=1;i<=3;i++)
	{
		if(i%2==1)
		{
			for(int j=3;j>=i;j--) {
				System.out.print("* ");
		}
		System.out.println();
	}}
	
	}}
		

